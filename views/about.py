"""项目说明：框架选型 / 分工 / 验收一页讲清"""
import streamlit as st
import theme

theme.apply()
theme.page_header("项目说明", "一个页面讲清楚：做什么、为什么这么选、怎么验收")

tab1, tab2, tab3 = st.tabs(["框架选型", "团队分工", "验收清单"])

with tab1:
    st.markdown("""
##### 为什么选 Streamlit
| 框架 | 优点 | 是否适合本组 |
|------|------|------|
| **Streamlit** | 纯 Python 写页面，零前端基础也能上手，最快成型 | 选定 |
| Gradio | 适合模型输入输出演示 | 备选 |
| Flask/FastAPI | 更工程化，但开发量大 | 否 |
| React/Vue | 能力强但门槛高，一周难跑通 | 否 |

**取舍**：放弃前后端分离，换取一周内能演示；AI 调用做了真实+模拟双模式，
把"API 不通"这个最大风险提前消化掉。
""")

with tab2:
    st.markdown("""
##### 角色分工（PPT 第 9 页）
- **PM（固定）**：拉齐分工、维护任务板、组织 Review 和演示
- **页面与集成**：搭 Web 入口和菜单，把工具拼到一起
- **功能开发**：各负责一个 AI 小工具，定义输入输出
- **提示词/API**：设计 prompt、接模型或模拟返回
- **测试/文档/演示**：写 README、验收清单、演示话术

> 分工不是边界：每个人都要能看懂代码、提交 Git、参与调试和演示。
""")

with tab3:
    st.markdown("""
##### 验收门槛（PPT 第 12 页）
**基础线（必须）**
- 仓库链接可访问
- Demo 本地能运行
- 至少 1 个功能完整闭环
- 每人能说明自己的贡献

**目标线（加分）**
- 说明框架选择理由
- 2-3 个工具集成到首页
- 有测试清单和已知问题
- 提示词集合可复用
""")
